# repl also called negative slicing 
name = "escanor" 
print(name[0:4]) # output will be esca

print(name[1:4]) # output will be sca

print(name[-4:-1]) # this will excluded -1 output is ano

print(name[:4]) # defaultly it will consider as 0:4 whose output will be esca

print(name[0:]) # defaultly it will consider as 0:6(total 7) output will be escanor